    public void departamentos(){
        depregistrados.setModel(deptos);
  
        Set <Datos> d = new HashSet<Datos>();
        d=  datosregiones.getDeptosreg();
       
        deptos.clear();
        for (Datos datos1 : d) {
         String agregar =  datos1.getDato2()+"/"+ datos1.getDato3()+
                          "/"+datos1.getDato1();
            
        deptos.addElement( agregar );
        }

        }
  
    public void municipios(){
         munregistrados.setModel(mostrarmun);
    
         ArrayList <DatosParaRegiones> m = new ArrayList <DatosParaRegiones>();
         m = datosregiones.getDatosreg();
         mostrarmun.clear();
         
         for (DatosParaRegiones datos2 : m) {
            mostrarmun.addElement(datos2.getCodigoMun()+ "/" +
                    datos2.getNombreMun()+"/"+ datos2.getNombreDep());
        }
         
    }
    
 
    
    public void precios(){
        Listaregiones.setModel(PrecioR);
        PrecioR.clear();
        
        ArrayList <Preciomod> Precios = new ArrayList<Preciomod>();
        Precios=PreciosReg.getPrecios();
        
        for(int i =0; i< Precios.size();i++) {
         String agregar =  Precios.get(i).getPestandar() 
                 + "/" + Precios.get(i).getPespecial();
            PrecioR.addElement(agregar);
        PrecioR.addElement("          ");
        } 
        
        
    }
    
   
    
