    private ArrayList<UsuariosDatos>  contarusuarios(){
        //Usuarios registrados 
        Set<UsuariosDatos> Usrs = new HashSet<UsuariosDatos>();
        Usrs = usuarios.getUsuarios();
  
       ArrayList<Datosenvios> Envios = new ArrayList<Datosenvios>();
       Envios=this.envios.getEnvios();
       
       // array de contador
       ArrayList<UsuariosDatos> cont = new ArrayList<>();
       
       
       
       int contador = 0;
    
        for (UsuariosDatos Usr : Usrs) {
            String u = Usr.getCorreo();
            for (Datosenvios en : Envios) {
                if(en.getNombreusuario().equals(u)){
                    contador += 1;
                }
            }
            UsuariosDatos agregar = new UsuariosDatos (u,Integer.toString(contador));
            cont.add(agregar);
        }
    
        return cont;
    }
    
    
