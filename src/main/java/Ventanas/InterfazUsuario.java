    private void TarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TarjetaActionPerformed


        String Nomtarjeta = this.NombreTarjeta.getText();
        String Numtarjeta = this.NumeroTarjeta.getText();
        String FechaVen = this.FechaVencimiento.getText();
        String nombre = usuarios.getUsuarioactual();
        
        if(Numtarjeta.length() < 15 || Numtarjeta.length() > 15 ) {
            JOptionPane.showMessageDialog(rootPane, "Numero de tarjeta incorrecto, se requieren 15 digitos");
        
        } else {
        
            Infcompra datos = new Infcompra (nombre,Nomtarjeta,Numtarjeta,FechaVen);
        regtarjetas.agregardatos(datos);
        
        listatarjetas();
        
        }
   
        
        
    }//GEN-LAST:event_TarjetaActionPerformed

