        public double preciototal(){
        /*
        Total de servicio = Precio de Región Destino (estándar o especial) * Tamaño de
        paquete * número de paquetes
        */  double pregion= calcularservicio();
            double total = pregion*getpeso()*getcant();
            double recargo = 0;
            String opcionpag = this.opcionpago.getSelectedItem().toString();
            if(opcionpag.equalsIgnoreCase("Pago contra entrega")){
             recargo = 5;
            }else{}
            total += recargo;
            return total;
        }
     
