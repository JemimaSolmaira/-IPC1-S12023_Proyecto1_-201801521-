    public void listatarjetas(){
        TarjetasRegistradas.setModel(listatarjetas);
       listatarjetas.clear();
        
        ArrayList <Infcompra> tarjetas = new ArrayList<Infcompra>();
        tarjetas = regtarjetas.getRegtarjetas();
        
        for(int i =0; i< tarjetas.size();i++) {
            
            String numero = tarjetas.get(i).getDato2();
            char a = numero.charAt(14);
            char b = numero.charAt(13);
            char c = numero.charAt(12);
            char d = numero.charAt(11);
            String oculto = "----------" + d + c + b+ a;
            String agregar =  tarjetas.get(i).getDato1() 
                 + " / " + oculto + " / " 
                    + tarjetas.get(i).getDato3();
            
         listatarjetas.addElement(agregar);
        listatarjetas.addElement("          ");
        } 
    
    }
    

