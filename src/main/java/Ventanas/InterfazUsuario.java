       public String buscarregion(String depto){
     String region = "";
        Set <Datos> d = new HashSet<Datos>();
        d = datosregiones.getDeptosreg();
     for (Datos datos : d) {
            if (datos.getDato3().equalsIgnoreCase(depto)){
            region = datos.getDato1();
            break;
            }
        }
        
     return region;
    
       }
    
    
        private double calcularservicio(){
         ArrayList <Preciomod> Precios = new ArrayList<Preciomod>();
        Precios=PreciosReg.getPrecios();
    
        String reg = buscarregion(departamento());
 
        double precioEncontrado = 0;
        int i = 0;

      if (reg.equalsIgnoreCase("Metropolitana")) {
      i= 0;
    } else if (reg.equalsIgnoreCase("Norte")) {
     i= 1;
    }else if (reg.equalsIgnoreCase("Nororiente")) {
     i= 2;
    }else if (reg.equalsIgnoreCase("Suroriente")) {
     i= 3;
    }else if (reg.equalsIgnoreCase("Suroccidente")) {
     i= 4;
    }else if (reg.equalsIgnoreCase("Noroccidente")) {
     i= 5;
    }

        if(servicio().equalsIgnoreCase("Estandar")){
      precioEncontrado =  Double.parseDouble(Precios.get(i).getPestandar());
    }else{
    
    precioEncontrado =  Double.parseDouble(Precios.get(i).getPespecial());
    }  
            
        return   precioEncontrado;  
      
        }
    
    
